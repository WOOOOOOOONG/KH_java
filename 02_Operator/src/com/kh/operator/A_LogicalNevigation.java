package com.kh.operator;

public class A_LogicalNevigation {
	public void method1() {
		System.out.println("true의 부정 : " + !true);
		System.out.println("false의 부정 : " + !false);
		
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println("b1 : " + b1);
		System.out.println("b2 : " + !b1);
		
		System.out.println("b2 : " + b2);
		System.out.println("b2 : " + !b2);
	}
}
